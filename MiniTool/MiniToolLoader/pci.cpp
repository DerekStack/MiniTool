PCI_VENDOR* FindVendorData(unsigned short id)
{
	PCI_VENDOR* result = nullptr;
	for (int i = 0; i < NO_OF_VENDOR; i++)
	{
		PCI_VENDOR item = PCIVendorHandler[i];
		if (item.VendorID == id)
		{
			result = &item;
			break;
		}
	}

	return result;
}

